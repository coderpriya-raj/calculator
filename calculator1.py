num1=float(input("Enter the first number\n"))
num2=float(input("Enter the second number\n"))
op=input("enter operation you want\n")

if op=="+":
    print(num1+num2)
elif op=="-":
    print(num1-num2)
elif op=="*":
    print(num1*num2)
elif op=="/":
    print(num1/num2)        
elif op=="**":
    print(num1**num2)
elif op=="%":
    print(num1%num2)  
elif op=="**":
    print(num1**num2)         
else:
    print("invalid input")   
